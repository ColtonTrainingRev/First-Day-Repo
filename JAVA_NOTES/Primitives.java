public class Primitives {
    /*
     * Java represents the most basic of data in structures called primitives: these data types are the simplest
     * ones you can work with in Java, and we will be making extensice use of them while we look at the other 
     * aspects of Java
     */
    // these primitive types represent whole numbers
    byte b = 0; //8 bits -- max size is 2^(8 - 1)
    int i = 0; //32 bits -- max size is 2^(32-1)
    short s = 0;//16 bits -- max size is 2^(16 - 1)
    long l = 0;//64 bits -- max size is 2^(64 - 1)
    //these primitive types represent numbers with decimals
    float f = 0f;//32 bits
    double d = 0;//64 bits
    //
    char c = 'c';
    boolean tf = true;
}
